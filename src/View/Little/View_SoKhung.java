    private boolean isMaSPExists(String maSP) {
        List<String> danhSachMaSP = S_CTSP.getSoKhung();
        return danhSachMaSP.contains(maSP);
    }

    public boolean checkForm() {

        if (txtSoKhung.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống số khung");
            return false;
        }

        try {
            String sk = txtSoKhung.getText();

            // Kiểm tra định dạng 5 số - 5 số bằng Regular Expressions
            if (!sk.matches("\\d{5}-\\d{5}")) {
              
                 JOptionPane.showMessageDialog(this,
                    "Số khung không hợp lệ, vui lòng nhập theo định dạng xxxxx-xxxxx (vd: 25373-36478)",
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
                return false;
            }
        } catch (NumberFormatException e) {

             JOptionPane.showMessageDialog(this,
                    "Số khung không hợp lệ, vui lòng thử lại",
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // Tiếp tục kiểm tra các trường khác nếu cần thiết
        return true;
    }
