    private D_KhachHang getForm() {
        String maKH = txtMaKhachHang.getText();
        String hoVaTen = txtHoVaTen.getText();
        String diaChi = txtDiaChi.getText();
        String sdt = txtSDT.getText();

        if (maKH.equals("") || hoVaTen.equals("") || diaChi.equals("") || sdt.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
            return null;
        } else if (sdt.matches("^[0-9]{10}$") == false) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng số điện thoại");
            return null;
        }
        return new D_KhachHang(maKH, hoVaTen, diaChi, WIDTH, sdt);
    }

    private boolean checkMaKH() {
        try {
            String maKH = txtMaKhachHang.getText();
            ArrayList<D_KhachHang> listKH = (ArrayList<D_KhachHang>) khachhangService.getAll();
            for (D_KhachHang kH : listKH) {
                if (kH.getMaKH().equalsIgnoreCase(maKH)) {
                    return true;
                }
            }
            return false;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi check mã khách hàng!", "Error!", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return false;
        }
    }

